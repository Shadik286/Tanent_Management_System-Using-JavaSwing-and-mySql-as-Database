    public void printingRevenue() {
        Connection conn = Connectionpdr.getCon();  
        DefaultTableModel mod = (DefaultTableModel)jTable1.getModel(); 
        int rowcnt = mod.getRowCount();
        for(Integer i = rowcnt - 1; i >= 0; i--) mod.removeRow(i);
        try{ 
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from rents");
            while(rs.next()) {
                String flatid = rs.getString("ApartmentID"), amount = rs.getString("Amount"); 
                String date = rs.getString("Date");  
                String Data[] = {flatid, date, amount}; 
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                model.addRow(Data);
            }
            
        } catch(Exception E) {
            JOptionPane.showMessageDialog(rootPane, "Unexpected Error Occured, Try Again...");
        }
    } 
    
