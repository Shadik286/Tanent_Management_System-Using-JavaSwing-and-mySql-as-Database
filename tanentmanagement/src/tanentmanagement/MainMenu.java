    public static Integer DBMS(String clmn) {
        Connection conn = Connectionpdr.getCon();  
        String query = "Select * FROM " + clmn;
        try {
            Statement st = conn.createStatement(); 
            ResultSet rst = st.executeQuery(query);  
            Integer cnt = 0;
            while(rst.next()) cnt++; 
            return cnt;
        } catch(Exception E) {
            JOptionPane.showMessageDialog(null, "Unexpactedly failled"); 
            return 0;
        }
    } 
    
